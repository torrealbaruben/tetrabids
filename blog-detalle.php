								<p>Un certificado SSL sirve para brindar seguridad al visitante de su página web, una manera de decirles a sus clientes que el sitio es auténtico, real y confiable para ingresar datos personales. Las siglas SSL responden a los términos en inglés (Secure Socket Layer), el cual es un protocolo de seguridad que hace que sus datos viajen de manera íntegra y segura, es decir, la transmisión de los datos entre un servidor y usuario web, y en retroalimentación, es totalmente cifrada o encriptada. El que los datos viajen cifrados, nos referimos a que se emplean algoritmos matemáticos y un sistema de claves que sólo son identificados entre la persona que navega y el servidor. Al tener un certificado SSL confiable, nuestros datos están encriptados, en ese momento podemos asegurar que nadie puede leer su contenido. Todo esto nos lleva a entender que la tecnología que brinda un certificado SSL es la transmisión segura de información a través de internet, y así confirmar que los datos están libres de personas no deseadas.</p>
								<h3>Para poder utilizar un certificado SSL, en su página web, es de vital importancia que el servidor de Internet que usted contrató, soporte SSL.</h3>
								<p>Estamos en la era de la información, el usuario o cliente necesita confiar en las transacciones realizadas a través de internet, el certificado SSL nos asegura que los datos son enviados al servidor correcto. Un certificado SSL es una tecnología que le brinda una gran solución de seguridad en línea, nos ayuda a garantizarles a los clientes que el sitio que están visitando es seguro; desde una simple visita, realizar compras o iniciar sesión.</p>
								<p>Un certificado SSL implementa el modelo preferido de seguridad en web, contiene claves digitales que protegen la integridad de sus datos al momento de enviar y recibir. Los servidores que corren SSL crean una vía con un cifrado único para las sesiones privadas a través que Internet, la clave pública del servidor está al alcance de cualquier persona. Es por eso que utilizan una clave pública y una clave privada: La clave pública es para cifrar la información, la clave privada para descifrarla.</p>
								<p>Al elegir un certificado SSL debe tomar cuenta los alcances deseados en su sitio web, la audiencia que tendrá acceso y por último el tipo de sesión que el usuario realizará. En la actualidad la mayoría de las aplicaciones web y servidores soportan un certificado SSL es por eso que le recomendamos analizar a profundidad la finalidad de su sitio web y haga una excelente decisión en cuanto a certificado SSL se refiere.</p>
